<h2>The walking trail</h2>
<h3>Distance and terrain</h3>
<p>The walking trail which leads around the site is 4 miles there and back. You will start out along a track, which can be very wet at any time of year, and will also venture off road and across and boggy moorland. The route is fairly flat with one short climb up to the summit of Y Pigwn, the highest point at 412m. Waterproof footwear with a good grip is recommended.</p>
