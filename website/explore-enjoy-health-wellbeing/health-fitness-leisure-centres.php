<p>
<img src="/all_images_in_here_please/outdoor_activity_images/health___wellbeing/p28.jpg" style="width: 704px; height: 471px; margin-top: 15px; margin-bottom: 15px; float: right;"/>Regular exercise can help protect you from heart disease and stroke, high blood pressure, diabetes, obesity, back pain and osteoporosis. It can also improve your mood and help you to manage stress and anxiety.</p>
<p>
<img src="/website/var/tmp/thumb_241__auto_9648f47a0ae2046361311f621276da76.jpeg" style="width: 300px; height: 204px; margin-left: 15px; margin-right: 15px; float: right;"/>For the greatest overall health benefits, experts recommend that you do 20 to 30 minutes of aerobic activity three or more times a week and some type of muscle strengthening activity and stretching at least twice a week.</p>
<p>However, if you are unable to do this level of activity, you can gain substantial health benefits by accumulating 30 minutes or more of moderate-intensity physical activity a day, at least five times a week.</p>
<p>If you have been inactive for a while, you may want to start with less strenuous activities such as walking or swimming at a comfortable pace. Beginning at a slow pace will allow you to become physically fit without straining your body. Once you are in better shape, you can gradually do more strenuous activity.</p>
