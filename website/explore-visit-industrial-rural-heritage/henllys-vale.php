<h1>Henllys Vale</h1>
<div class="article-video right clearfix"/>
<p/><p>
<span style="font-size:18px;">At Henllys Vale, next to the Afon Twrch in the southern part of the Brecon Beacons National Park, five lime kilns and the Brynhenllys coal mines provided a livelihood for the local community.</span></p>
<p>This part of the Fforest Fawr Geopark used to be a hive of industrial activity. The colliery operated between 1872 and 1956, with waterwheels to do the winding and pumping. Miners slaved underground while women took panniers up into the Black Mountain to carry silica sand.</p>
<p>The vale is now a pleasant place to walk. You can follow the route of the old colliery tramline across the moorland. The lime kilns are still visible but virtually all that's left of the colliery is an impressive chimney.</p>
<p>The footpath follows the east bank of the Afon Twrch (which translates as River Boar). The river is home to a range of wildlife including dippers, a small, white-breasted bird which lives beside fresh water.</p>
<h2>Visiting Henllys Vale</h2>
<p>The site is open to the public. Access is free.</p>
