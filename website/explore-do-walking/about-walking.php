<h3>How to get there</h3>
<p>When planning a route, why not use the bus to get you to the start, back from the finish, or both? You can cover far more ground that way. The summer Beacons Bus service is designed with walkers in mind. Local services can also be helpful. If you do use your car, be sure not to leave any valuables in it when parking in the countryside. Sadly, vehicle crime is an ever-present problem.</p>
