<h1>Church of St Issui, Patricio (or Partrishow)</h1>
<div class="article-video right clearfix"/>
<p/><p>
<span style="font-size:18px;">The charming little church in Patricio, sometimes spelt Partrishow, is situated on the southern slopes of the Gader Range, with a wonderful panoramic view of the lower Grwyne Fawr and the Nant Mair stream flowing below.</span></p>
<div id="parent-fieldname-text">
<p>
