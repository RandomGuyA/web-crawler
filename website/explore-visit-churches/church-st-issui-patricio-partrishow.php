<p>The church is named after St Issui, an early Celtic saint who is thought to have built a cell here. The site became a place of pilgrimage after he was murdered.</p>
<p>Among the church's treasuresÂ are an exquisite rood screen,Â carved out of Irish oak, and some interesting medieval wall paintings.</p>
<p>At the rear of the churchÂ is an unusual dugout parish chest carved out of a solid tree trunk, with iron bands around it and three locks. It was once used for the safe keeping of parish valuables. The Rector and Church Wardens each held a key to one of the three locks, so the chest could only be opened when all three were present.</p>
<p>The churchyard was originally very small, but has been extended in the last hundred years and now stretches as far as the lane. It retains a medieval churchyard cross and is still used for modern burial.</p></div>
