<h1>Capel-y-Ffin Monastery</h1>
<div class="article-video right clearfix"/>
<p/><p>
<span style="font-size:18px;">Close to the ruins of Llanthony Priory are the remains of a 19th century monastery, part of which is now a house, the former home of the sculptor Eric Gill.</span></p>
<p>The Reverend Joseph Leycester Lyne, known as Father Ignatius, tried to buy Llanthony Priory from its 19th century owners, the Landor family, but failed. So on St Patrick's Day 1870, Father Ignatius laid the foundation stone of Llanthony Monastery of Our Ladye and St Dunstan, Llanthony Tertia, at Capel-y-Ffin.</p>
<p>He wanted to revive the Benedictine movement in Wales as it is said that he saw a vision of the Virgin Mary in a burning bush here. Francis Kilvert, in his diary, tells of the dissatisfied tenants at Llanthony on rent day and he also watched the monks building their church.</p>
<p>Father Ignatius eventually died and was buried in his church, but the building soon became neglected, and today is open to the skies. There is a pilgrimage held annually in August (usually on the Saturday of the August Bank Holiday weekend) from Llanthony to Capel-y-Ffin.</p>
<p>Part of the monastery is now a private residence and it is here that Eric Gill, the sculptor and typeface designer, lived with his family. It is possible to visit the remains of the church and a small chapel made in the domestic buildings in Gill's time.</p>
