<h1>Dan-yr-Ogof and the National Showcaves Centre for Wales</h1>
<div class="article-video right clearfix"/>
<p/><p>
<span style="font-size:18px;">In the Upper Swansea Valley anyone, even absolute beginners with no knowledge of caving, can get a taste of the underworld. You can even get married here!</span></p>
<p>This is one of the most popular visitor attractions in the Brecon Beacons National Park. The centre gives you a glimpse of the most accessible sections of a cave system which extends for over ten miles underground. Itâ€™s one of the most significant natural networks of subterraneanÂ passages and caverns in Britain. It attracts experienced cavers as well as beginners, and has been used as a location for Doctor Who (BBC).</p>
<p>There's evidence that people lived and buried their dead in these caves in the Bronze Age, over 3000 years ago. Over 40 human skeletons have been found.</p>
<p>Recent exploration of the system was pioneered by two local brothers, Tommy and Jeff Morgan, who entered the caves in 1912 using candles and the most basic equipment. They used a coracle to cross underground lakes. Their descendants now own the Showcaves.</p>
<p>Visitors enter through a mined tunnel which gives access to a number of impressive areas. The Cathedral Cave, which is 150m below ground and 10m high at one point, is a highlight. Uniquely, it's licensed for marriage ceremonies.</p>
<p>Around 1km of safe pathways lead through the caverns. They're well lit so that you can admire the texture of the rock and the many stalagmites and stalactites. There's recorded music and commentaries to add extra atmosphere. Reconstructions show how Neanderthal cave-dwellers used to live and bury their dead here in shallow graves.</p>
<p>Beyond the public areas is an extensive cave system that's a National Nature Reserve. This is only accessible to experienced caving club members.</p>
<h2>Visiting the National Showcaves Centre</h2>
<p>The admission fee covers the caves and numerous other attractions at the centre.</p>
<h3>How to get there</h3>
<p>The centre is signposted from M4 junction 45 and from Brecon via the A40 and A4067. It's just north of Craig-y-Nos Country Park.</p>
