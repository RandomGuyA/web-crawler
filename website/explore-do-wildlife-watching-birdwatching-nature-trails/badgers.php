<h1>Badgers in the Brecon Beacons National Park</h1>
<div class="article-video right clearfix"/>
<p/><p>
<span style="font-size:18px;">Badgers are instantly recognisable but rarely seen, being shy and nocturnal.</span></p>
<p>They are the heaviest of our wild predators, the adult males weighing over 11kg, and yet they eat mostly earthworms, insects, roots and berries.</p>
<p>They are highly social, living in networks of underground tunnels called setts. These are often found in and around woodland, railway embankments and road verges. Badgers are creatures of habit and forage and roam along regularly used paths.</p>
<p>Badgers and their setts are protected under the Protection of Badgers Act, 1992, which makes it illegal to kill, injure or interfere with a badger or disturb a sett without a licence, issued under strict conditions.</p>
