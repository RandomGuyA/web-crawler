<p>This is one of the all time classics of the Brecon Beacons taking in the Monmouthshire and Brecon Canal, the Taff Trail, the Brinore Tramway and the very popular Gap Road.</p>
<p>The terrain is rideable in all seasons but beware in bad weather conditions. The Gap takes you up to 600m above sea level and is very open and windswept. It is essential to take food, adequate clothing and tools before you attempt this route.</p>
<p>Technically, the going is mostly straightforward, with a tricky ravine halfway up the Gap road and some big rock steps on beginning the descent from the Gap itself. Also worthy of note is the descent from the top of the Brinore Tramway to the valley bottom just up stream of the Talybont Reservoir. The track varies from smooth grass to start with, to gravel, rocks and ruts. Finally, at the bottom, you have to ford the river where floods have washed away the bridge. In periods of high water levels, opt for a short cut along the Taff Trail to the top end of the forest.</p>
