<h1>Stay safe when cycling and mountain biking in the Brecon Beacons National Park</h1>
<div class="article-video right clearfix"/>
<p/><p>
<span style="font-size:18px;">To get the most out of your time on two wheels in the Brecon Beacons National Park, we recommend that you follow our top tips for safety and good practice.</span></p>
<h2>
<img src="/website/var/tmp/thumb_1438__auto_1f94b56e8feb8091fd5718b75caf6323.jpeg" style="width: 704px; height: 466px; margin-top: 15px; margin-bottom: 15px; float: right;"/>Take care</h2>
<ul>
<li>
<p>Always wear a helmet</p></li>
<li>
<p>Ride within your capabilities and skills and allow plenty of time for your ride</p></li>
<li>
<p>Before setting out, ensure your bike is safe by checking tyres, brakes and gears</p></li>
<li>
<p>Tell someone where you are going and what time you intend to returnÂ </p></li>
<li>
<p>Take the appropriate OS map, compass, basic first aid kit, food and money</p></li></ul>
<h2>Tools</h2>
<ul>
<li>
<p>Always carry a basic tool kit: a cycling specific multi-tool is best, but if not you will need 4mm, 5mm and 6mm Allen keys, small flat head and Phillips screwdrivers</p></li>
<li>
<p>Also make sure you carry a pump, tyre levers, puncture repair kit and some spare inner tubes - it is easier to replace a tube than to fix a puncture</p></li></ul>
<h2>Clothing</h2>
<ul>
<li>
<p>Make sure you are prepared for all weather conditions by wearing several layers of clothing that can be adjusted</p></li>
<li>
<p>Take a lightweight waterproof jacket, even in fine weather</p></li>
<li>
<p>Padded cycling shorts can be worn under leggings in cold conditions</p></li></ul>
<h2>Right to ride</h2>
<p>In the UK, the only places where you can legally ride off-road are byways, bridleways, National Cycle Network routes (
<a href="http://www.sustrans.org.uk" target="_blank">www.sustrans.org.uk</a>) and designated trails on private land such as forests, mountain bike centres and the banks of reservoirs.</p>
<p>Cyclists and mountain bikers must give way to horses, riders and walkers on bridleways, towpaths and other footpaths.</p>
<h2>Code of conduct</h2>
<h2>
<img src="/website/var/tmp/thumb_173__auto_95c22c1ba81a9d77065a83850fc6a148.png" style="width: 200px; height: 300px; margin-left: 15px; margin-right: 15px; float: right;"/></h2>
<p>Many different people live in the Brecon Beacons National Park or come here to enjoy all sorts of pastimes. Please be considerate and courteous to all those you meet en route. It's particularly important to slow down and give way to pedestrians. The same goes for to horse riders: failure to do so can result in horses being terrified, bolting and the rider being seriously injured. Also, bear in mind that the Brecon Beacons is a National Park and that we all have a duty to conserve the natural beauty of this special place.</p>
<ul>
<li>
<p>Give way to pedestrians and horse riders</p></li>
<li>
<p>On public roads, respect the rules of the road</p></li>
<li>
<p>Don't interfere with wildlife, plants or trees</p></li>
<li>
<p>Keep to published routes</p></li>
<li>
<p>Always leave gates as you find them</p></li>
<li>
<p>Take rubbish with you</p></li>
<li>
<p>Don't light fires</p></li>
<li>
<p>Keep away from Forestry operations</p></li>
<li>
<p>Expect the unexpected</p></li>
<li>
<p>Avoid back wheel skids which can start erosion</p></li>
<li>
<p>Slow down when approaching and travelling through farmyards</p></li></ul>
