<h2>Llanfoist to Clydach Gorge</h2>
<p>A well surfaced section of traffic free cycle path that can be ridden in its entirety from Llanfoist to Clydach. Follows the course of an old railway line, views from the canal on the lower section and across the Clydach Gorge are spectacular.</p>
<h5>
<span style="color:#808080;">Start: Llanfoist Crossing, OS reference SO286132</span></h5>
<h5>
<span style="color:#808080;">Distance: 7.5km / 5 miles each way</span></h5>
<h5>
<span style="color:#808080;">Gradient: long, steady climb with one very steep section near the end</span></h5>
<h5>
<span style="color:#808080;">Time: 1.5hrs outward and 1hr back</span></h5>
<h5>
<span style="color:#808080;">Suitable for: most bikes except tandems and other long/wide bikes.</span></h5>
<h5>
<span style="color:#808080;">Parking: large car park at Llanfoist Crossing</span></h5>
<h5>
<span style="color:#808080;">Public toilets: in Abergavenny</span></h5>
<h5>
<span style="color:#808080;">Other facilities: none</span></h5>
<p>Head through the gate at the top of the car park and follow the well-marked trail upwards, passing close to the canal. Keep ahead at Govilon, where the trail passes through houses and crosses a public road and then passes the Forge car park before starting to climb steeply up towards Clydach. Thereâ€™s a section on public roads before the railway climbs to Brynmawr.</p>
