<h2>Usk Reservoir Trail</h2>
<p>A ride around the reservoir.</p>
<h5>
<span style="color:#808080;">Start: Glasfynydd Forest, OS reference SO820271</span></h5>
<h5>
<span style="color:#808080;">Distance: 10km / 6 miles</span></h5>
<h5>
<span style="color:#808080;">Gradient: some hills</span></h5>
<h5>
<span style="color:#808080;">Time: 2hrs</span></h5>
<h5>
