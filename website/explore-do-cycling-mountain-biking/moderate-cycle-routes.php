<h5>
<span style="color:#808080;">Parking: large car park at Glasfynydd Forest</span></h5>
<h5>
<span style="color:#808080;">Public toilets: none</span></h5>
<h5>
<span style="color:#808080;">Other facilities: picnic tables at Glasfynydd Forest</span></h5>
