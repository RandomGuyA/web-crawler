<br/>Directions:</div>
<ul>
<li>Start in Brecon town centre go over the Usk Bridge and along Bridge Street. Turn left onto Ffrwdgrech road (opposite a pub) and under the A40. Fork right at next junction towards Cilwhybert.</li>
<li>Just past the "Motte" at Cilwhybert turn right. Cross river. Straight ahead at A470 crossroads (take care) and up a steep lane to T junction. Turn left to cycle uphill. Over cattle grid and keep left. Turn left at next junction to visit the Visitor Centre, otherwise go straight ahead.</li>
<li>Downhill (over the route of Sarn Helen). Keep left and uphill to the junctionwith the A4215. Turn left signposted Brecon, after ½mile turn right (take care) signposted Heol Senni.</li>
<li>At Heol Senni turn right signposted Defynnog. At the junction with the A4125turn left. In Defynnog turn right on the A4067 to Sennybridge. Pass church, after quarter mile fork left into Sennybridge.</li>
<li>At the centre of Sennybridge turn right at the main road (A40). After 200m turn first left signposted to Pentre’r-felin and cross old stone bridge over the River Usk.</li>
<li>After ½ mile take the first right to follow the Usk valley along tiny lanes, cross the river (Cileni). At next T- junction turn right signposted Brecon.</li>
<li>Cycle through Trallong and down to Aberbran, cross the river then turn to Aberyscir and Cradoc. At Cradoc turn right signposted Brecon (good views of Pen y Crug hill fort). Cycle round the hill and down Cradoc road into Brecon.</li></ul></div>
