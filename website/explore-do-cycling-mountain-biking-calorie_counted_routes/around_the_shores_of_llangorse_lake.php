<div>This route is ideal for those who are confident riding along lanes which in some places are narrow, starts from the Brecon Canal Basin and takes you to Llangorse Lake. This will take about half a day with a few challenging hills.</div>
<h4>
<br/>Directions:</h4>
<div>From the Canal basin follow the towpath along the south side of the canal to Brynich Lock.</div>
<div>
<br/>Turn left at the main road and cycle under the A40. Just beforeGroesffordd turn right, signposted for Llangorse &amp; Llanfihangel.</div>
<div>
<br/>At the top of the hill, turn right to Talyllyn. Follow this small lane, with really good views across Llangorse Lake and the "Crannog". Turn right at Talyllyn to Pennorth.</div>
<div>
<br/>At Pennorth, turn left, signposted for Bwlch and go over the old railway line. Just past the Motte, turn left to visit St Gastyns Church the lake and pic-nic area.</div>
<div>
<br/>Cycle back up the lane –very good views of the big country house at Treberfydd, with walled garden and large established ornamental trees. Turn left to continue on the main cycle route. After ½ a mile turn next left, signposted Cathedine.</div>
<div>
<br/>At the T-junction with the B4560 turn left to Cathedine church and LLangorse (take Care). Go through Llangorse village, past St Paulinus Church and turn left signposted Brecon and Lake. Turn first left to the Lake, go over the cattle grid and onto Llangorse Common and lakeside shop.</div>
<div>
<br/>From the common, return across the cattle grid and turn left on the main road to Llanfihangel Tal-y-Llyn, be very careful on this section as the road is narrow and cars travel fast at times.</div>
<div>
<br/>Go through the village and turn first left, signposted Talyllyn. At Talyllyn, turn right and return to Brecon via Groesffordd and Brynich Lock.</div></div>
