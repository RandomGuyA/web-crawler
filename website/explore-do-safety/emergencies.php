<h1>What to do in an emergency in the Brecon Beacons National Park</h1>
<div class="article-video right clearfix"/>
<p/><p>
<span style="font-size:18px;">Most people enjoy a safe visit to the countryside of the National Park. But it's important to know what to do if the worst does happen.</span></p>
<p>Do not try to move someone who may be badly injured or suffering from hypothermia (exposure). Keep them warm and dry and try to summon help. If you have to fetch help, leave someone with the casualty.</p>
<p>To contact the Emergency Services:</p>
<ul>
<li>Dial 999 and ask for POLICE</li>
<li>Tell them about the injury and when it happened</li>
<li>Tell them where the casualty is (preferably a map or GPS reference)</li>
<li>Stay by the phone until the trained rescue team or police arrive, so that you can keep in touch with the emergencyÂ coordinators</li></ul>
<p>If calling from a mobile phone, be ready to give your precise location immediately. The operator may be based at a central office some distance away and unfamiliar with the area.</p>
<p>If you cannot get to a phone use the international distress signal - six blasts on a whistle, shouts or flashes of a torch. Repeat as necessary after a minute's pause. Three back is the reply.</p>
<p>If you get caught out on the hills in bad weather, watch for signs of hypothermia. These include fatigue, shivering, stumbling, slurred speech and erratic behaviour.</p>
