<span style="color:#008000;"><span style="font-size:14px;"><span style="font-family:trebuchet ms,helvetica,sans-serif;"><strong>Further information</strong></span></span></span></p>
<p>
<span style="font-size:14px;"><span style="font-family:trebuchet ms,helvetica,sans-serif;">This is an excellent sheltered spot for wildlife including birds &amp; dragonflies. This is an isolated, but scenic spot in the middle of a forested valley. A short ramp with a gradient of 1:4 leads onto the boardwalk. Mobile phone reception is poor in this area.</span></span></p>
<p>
<span style="font-size:14px;"><span style="font-family:trebuchet ms,helvetica,sans-serif;"><span style="color:#008000;"><strong>How to get there:</strong></span>This is an isolated spot on a minor road, 5km north of Pontsticill - follow the signs for Talybont-on-Usk. The board walk is just past the Ystradgynwyn tea rooms and holiday cottages (
<span class="srsatxt" dir="ltr">CF48 2UT)</span>and are clearly visible on your left hand side.</span></span></p>
<p>
<span style="font-size:14px;"><span style="font-family:trebuchet ms,helvetica,sans-serif;"><span style="color:#008000;"><strong>Nearest town or village:</strong></span>Pontsticill.</span></span></p>
<p>
<span style="font-size:14px;"><span style="font-family:trebuchet ms,helvetica,sans-serif;"><span style="color:#008000;"><strong>OS Grid Reference:</strong></span>Explorer Map OL12 or Landranger Map 160 - SO 048 161.</span></span></p>
<p>
<span style="font-size:14px;"><span style="font-family:trebuchet ms,helvetica,sans-serif;"><span style="color:#008000;"><strong>Distance:</strong></span>300 metres there and back.</span></span></p>
<p>
<span style="font-size:14px;"><span style="font-family:trebuchet ms,helvetica,sans-serif;"><strong>Contact</strong>: Natural Resources Wales on 0300 068 0300.</span></span></p>
<p>
<span style="font-size:14px;"><span style="font-family:trebuchet ms,helvetica,sans-serif;"><span style="color:#008000;"><strong>Facilities</strong>:</span>There are a number of accessible picnic facilities nearby and further way-marked walks in the area. The closest facilities to this site are at Merthyr Tydfil and local shops can also be found at Trefechan.</span></span></p>
<p>
<span style="font-size:14px;"><span style="font-family:trebuchet ms,helvetica,sans-serif;"><span style="color:#008000;"><strong>Parking</strong>:</span>The compacted stone car park holds three cars and is at the start of the boardwalk. There is a larger car park 150 metres further north. As with all car parks we strongly recommend that you donâ€™t leave valuables in your car.</span></span></p>
<p>
<span style="font-size:14px;"><span style="font-family:trebuchet ms,helvetica,sans-serif;"><span style="color:#008000;"><strong>Toilets:</strong></span>The closest toilets can be found in Merthyr Tydfil and Talybont-on-Usk.</span></span></p></div>
