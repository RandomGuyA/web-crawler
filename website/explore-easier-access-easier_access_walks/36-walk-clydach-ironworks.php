<h1>36. A stroll around Clydach Ironworks and Picnic Site</h1>
<div class="article-video right clearfix"/>
<p><span style="font-size:14px;"><span style="font-family: trebuchet ms,helvetica,sans-serif;">The ruins of this blast furnace, in the lower end of the Clydach Gorge, were an important site in the early industrial revolution.
<br/>
<br/>
<span style="color:#008000;"><strong>Further Information</strong></span><br/>
<br/>The excavated and semi-restored remains are now protected as a Scheduled Ancient Monument and can be safely seen from the fence. The site is signposted from the entrance to the car park: turn right out of the car park, head along the tarmac road for 20 metres, turn right and cross the stone bridge and up the compacted stone track for a further 200 metres and then over the old iron bridge into the ironworks. A picnic and barbecue area is located in the parking area.
<br/>
<br/>
<span style="color:#008000;"><strong>How to get there:</strong></span>Clydach is signposted on the A465 between Brynmawr and Gilwern. The car park is on the left hand side after you descend from the village.
<br/>
<br/>
<span style="color:#008000;"><strong>Nearest town or village:</strong></span>Clydach or Gilwern.
<br/>
<br/>
<span style="color:#008000;"><strong>OS Grid Reference</strong>:</span>Explorer Map OL 13 or Landranger Map 161 - SO 230 133.
<br/>
<br/>
<span style="color:#008000;"><strong>Distance</strong></span>: 300 metres from the car park (one way).
<br/>
<br/>
<span style="color:#008000;"><strong>Contact:</strong></span>Monmouthshire County Council on 01633 644856.
<br/>
<br/>
<strong><span style="color:#008000;">Facilities</span>:</strong>There are no facilities at this site. The nearest facilities are in Gilwern.
<br/>
<br/>
<span style="color:#008000;"><strong>Parking:</strong></span>There are 4 spaces on a level tarmac lay-by, with a further 10 spaces on the main compacted stone car park.
<br/>
<br/>
<span style="color:#008000;"><strong>Toilets:</strong></span>The nearest public toilets are at Gilwern and the closest disabled access toilets (RADAR) are in Brynmawr</span></span></p>
