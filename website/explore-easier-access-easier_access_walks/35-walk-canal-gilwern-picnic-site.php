<h1>35. A stroll around Gilwern Wharf Picnic Site</h1>
<div class="article-video right clearfix"/>
<p/><div>
<span style="font-size: 14px;"><span style="font-family: trebuchet ms,helvetica,sans-serif;"><span style="color: rgb(105, 105, 105);">A well-used picnic site alongside the Monmouthshire and Brecon Canal with two picnic tables, one with an overhang, situated on a level grassed area. This site is a good starting point for strolls along the canal towpath.</span><br/>
<br/>
<strong><span style="color: rgb(51, 153, 0);">Further Information</span></strong><br/>
<br/>
<span style="color: rgb(105, 105, 105);">The towpath stretches for several miles or kilometres in both directions and is surrounded by fields and woods. There is a tarmac path to a viewing point which is 470 metres to the west with views across woodland, fields, the Sugar Loaf and the Black Mountains. The path heading towards the village (east) becomes narrow (70 centimetres) and bumpy for a short stretch. Gilwern village is 300 metres away along the towpath.</span><br/>
<br/>
<strong><span style="color: rgb(51, 153, 0);">How to get there:</span></strong><span style="color: rgb(105, 105, 105);">From Gilwern, take the A4077 Crickhowell Road. Just past the garage, turn up Church Road, which is signposted: Llanelly Church: 1.5 miles. The entrance is 300 metres uphill on the right hand side.</span><br/>
<br/>
<span style="color: rgb(51, 153, 0);"><strong>Nearest town or village:</strong></span>
<span style="color: rgb(105, 105, 105);">Gilwern.</span><br/>
<br/>
<span style="color: rgb(51, 153, 0);"><strong>OS Grid Reference:</strong></span>
<span style="color: rgb(105, 105, 105);">Explorer Map OL 13 or Landranger Map 161 - SO 243 146.</span><br/>
<br/>
<span style="color: rgb(51, 153, 0);"><strong>Distance</strong>:</span>
<span style="color: rgb(105, 105, 105);">A choice from 100 metres to several kilometres.</span><br/>
<br/>
