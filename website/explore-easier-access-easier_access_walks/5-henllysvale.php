<span style="font-size: 14px;"><span style="font-family: trebuchet ms,helvetica,sans-serif;"><strong><span style="color: rgb(105, 105, 105);">This audio trail takes you along an almost level path from Bryn-henllys Bridge to the former colliery at Henllys Vale with its imposing chimney and limekilns. The footpath offers a smooth and continuous surface along the majority of its length. However, short and moderately steep natural inclines either side of stop four can be avoided by following the path alongside the river
<span style="font-weight: bold;">.</span></span></strong><br/>
<br/>Stop 1 OS Grid Ref: SN 756 126
<br/>
<br/>Explorer Map or Landranger Map 160
<br/>
<br/>1. Lower Brynhenllys Colliery
<br/>
<br/>Henllys Vale is just off the A4068 at Ystradowen between Ystradgynlais and Brynaman. The start of the walk can be reached by turning off by the bus stop on the corner of the main road (A4068) in Ystradowen. Follow this road past the park and down a hill to the stone bridge (Brynhenllys Bridge). Cross the bridge and listen to track one besides the large boulders near the kissing gate.
<br/>
<br/>2. Hole in the wall
<br/>
<br/>With the river to your left, head through the kissing gate and along the path upstream. Within two minutes you will see a stone wall to your right. Listen to track 2 when you see an arched hole in this wall.
<br/>
<br/>3. Flashy Floods &amp; Sparky Rivers
<br/>
<br/>Continue along the path and soon the river will pull away to the left. Listen to track 3 when you reach a large stone slab overhanging the tramway above, and to your right.
<br/>
<br/>4. You are now going to leave the main path and head up the grassy track on the right. Listen to track four when the weir, in the river below, is directly to your left.
<br/>
<br/>5. Opencasting &amp; Rotten Eggs
<br/>
<br/>Continue along the path, over the brow and back down to rejoin the main path. Listen to track 5 approximately 20 metres before the grassy area on the right gives way to woodland.
<br/>
<br/>6. Ice Age Boulders &amp; Rotten Stones
<br/>
<br/>Carry on along the path and as you pass another weir see if you can work out how high it would have been. Listen to track 6 at the second bridge you encounter.
<br/>
<br/>7. Wild Boars &amp; Runaway Trams
<br/>
<br/>Listen to track 7 after crossing the bridge onto the other side of the river.
<br/>
<br/>8. Geological layers
<br/>
<br/>Follow the path alongside the river (and not the path through the gate to the left), go through a bridle gate and listen to track 8 by the exposed rock face a further 20 metres ahead.
<br/>
<br/>9. Henllys Vale Colliery
<br/>
<br/>Follow the path ahead, keeping the fenceline on your left and listen to track 9 when you have a full view of the chimney from base to top.
<br/>
<br/>10. Limekilns
<br/>
<br/>Head towards the stone structure built into the bank and listen to track 10 by the interpretation panel.
<br/>
<br/>Return to Lower Brynhenllys Colliery
<br/>
<br/>Return back to the car park along the same route you came.</span></span><br/>
