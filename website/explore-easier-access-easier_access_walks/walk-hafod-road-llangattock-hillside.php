<h1>A stroll along the Hafod Road and Llangattock Hillside</h1>
<div class="article-video right clearfix"/>
<p>Perched above the Usk Valley overlooking Crickhowell and Gilwern, the 'Hafod' road and Llangattock Hillside offer a mix of walks along a level, minor road and the chance to explore the limestone quarries along gently sloping quarry tracks.
<br/>
<br/>
<span style="color:#006400;"><strong>Further information:</strong></span><br/>
<br/>The road itself was used to transport limestone to the iron-makers in Brynmawr and Ebbw Vale, whilst other routes lead downhill to the Monmouthshire and Brecon Canal. The views on a good day extend across to the Black Mountains and into England. This route is reasonably well used by cyclists and local people, with the quarries regularly used by rock climbers.
<br/>
