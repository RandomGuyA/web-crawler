<h1>13. A stroll on Mynydd Illtud Common</h1>
<div class="article-video right clearfix"/>
<p><span style="font-size:14px;"><span style="font-family: trebuchet ms,helvetica,sans-serif;">There are plenty of options for easy walks on the common, with views of the surrounding mountains and lots of wildlife to spot.
<br/>
<br/>
<span style="color:#008000;"><strong>Further Information</strong></span><br/>
<br/>Most of the paths are made up of short grass, compacted mud tracks, but you can even use the minor road that runs across the common. There are wheel ruts in some areas of the common. The common is grazed by horses, ponies and sheep most of the year round. Please note that mobile phone reception is poor in the area.
<br/>
<br/>
