<br/>Between the years of 1922 and 1959 Craig-y-nos Castle was a tuberculosis sanatorium. Patients often stayed for years only seeing visitors once a month and treatment was basic, bed rest, good food and fresh air. Tuberculosis could be fatal, but the development of the antibiotic streptomycin in the 1940s meant that finally there was a cure.</p>
<br/>Many of the patients who were in Craig-y-nos were children and this exhibition tells some of their stories. With archive photos, artefacts and a short oral history film with former patients sharing their memories of their time in Craig-y-nos, the staff who worked there, the treatment they got and the friends that they made.
