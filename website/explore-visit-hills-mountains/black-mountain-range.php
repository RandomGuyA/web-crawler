<h1>The Black Mountain range</h1>
<div class="article-video right clearfix"/>
<p/><p>
<span style="font-size:18px;">The Black Mountain range is the most westerly of the Brecon Beacons National Park sandstone uplands, straddling the county boundary between Carmarthenshire and Powys. It forms part of the Fforest Fawr Geopark.</span></p>
<p>
<img src="/website/var/tmp/thumb_2765__auto_450078387bf1c6122aa1db3e318c6c98.jpeg" style="width: 704px; height: 528px; margin-top: 15px; margin-bottom: 15px; float: right;"/>This wild, remote, thinly populated region is not to be confused with the better-known Black Mountains in the east, or the peak called Black Mountain, south of Hay-on-Wye, which is in the Black Mountains.</p>
<p>The Black Mountain range stretches approximately from Ammanford in the south-west to Sennybridge in the north-east. It culminates in the summit of Fan Brycheiniog (802m), contains the two enchanting glacial lakes of Llyn y Fan Fach and Llyn y Fan Fawr, and is separated from the Fforest Fawr massif by the Tawe valley. The sources of both the River Tawe and the River Usk are found on the eastern flanks of the range.</p>
<p>In the most part, these uplands are formed from old red sandstone, with spectacular north and east facing scarps in the north-eastern section. There are also hard, erosion-resistant bands of carboniferous limestone and millstone grit in the south and west of the range.</p>
<p>The surrounding region has a fascinating cultural heritage and is the stronghold for the Welsh language in the National Park.</p>
