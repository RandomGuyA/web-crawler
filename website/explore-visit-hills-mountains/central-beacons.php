<h1>The Central Beacons</h1>
<div class="article-video right clearfix"/>
<p/><p>
<span style="font-size:18px;">The Brecon Beacons, from which the Brecon Beacons National Park gets its name, are often referred to as the Central Beacons. They stand at the heart of our Park, dominating the skyline to the south of Brecon and rising to 886 metres at Pen-y-Fan, the highest point in southern Britain.</span></p>
<p>
<img src="/website/var/tmp/thumb_868__auto_f54c07be69cbb90f4e2fc21192a586ff.png" style="width: 704px; margin-top: 15px; margin-bottom: 15px; float: right; height: 458px;"/>The range comprises six old red sandstone peaks. From west to east, these are: Corn Du (873m), Pen-y-Fan (886m), Cribyn (795m), Fan-y-Big (719m), Bwlch y Ddwyallt (754m) and Waun Rydd (769m). These summits form a long ridge. The first four stand in a horseshoe shape around the head of the Taff Fechan river, which flows down to the south-east and feeds the Neuadd and Pontsticill reservoirs. To the east is Talybont Reservoir.</p>
<p>The distinctive shape of Pen-y-Fan makes it easily recognisable. It's hugely popular with walkers. Corn Du, which is very similar in shape and height, is Pen-y-Fan's twin; in the past they formed a single twin-peaked summit.</p>
<p>Cribyn's steep approach from the north-east offers walkers an enjoyable challenge - the reward is superb views of Pen-y-Fan, which can be climbed in the same hike. Waun Rydd, the easternmost of the four principal peaks, has great views over both the Central Beacons and the Black Mountains.</p>
<p>The Central Beacons are separated from the Fforest Fawr massif by the Taff Fawr valley and its reservoirs.</p>
