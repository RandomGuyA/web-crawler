<h1>Crickhowell Castle</h1>
<div class="article-video right clearfix"/>
<p/><p>
<span style="font-size:18px;">Crickhowell Castle, also known as Alisby's Castle, is a prominent feature of the small market town of Crickhowell. Once a mighty stronghold, it had commanding views along the Usk Valley.</span></p>
<p>
<img src="/website/var/tmp/thumb_909__auto_dd027cfed0381323eba2fc55b0b699b5.jpeg" style="width: 704px; height: 449px; margin-top: 15px; margin-bottom: 15px; float: right;"/>Crickhowell Castle was built by the Normans. It is thought that it begun life as a motte and bailey castle with timber buildings in the 12th century, then in 1272 it was rebuilt in stone by Sir Grimbald Pauncefote.</p>
<p>During the 14th century the castle was in Mortimer hands but in 1402 it was restored to Sir John Pauncefote, great-grandson of Sir Grimbald, who refortified it by royal command. He was nonetheless unable to resist Owain Glyndwr's forces who left it in ruins. Later in the century it was granted to Sir William Herbert, who became the Earl of Pembroke. It seems unlikely that any further work was done.</p>
<p>A stone shell keep was provided around the top of the motte, slight traces of which are still visible. The base of the motte and bailey were also walled and provided with towers and gateways; only two substantial masonry fragments survive. The one to the southeast of the motte (not a keep as described in some sources) is a strong double tower provided at the eastern corner of the bailey where it adjoined the motte. One side of this survives to a considerable height.</p>
<p>More ruinous is the gatehouse to the southwest of the motte. This originally consisted of a pair of drum towers, one of which remains to an apparently precarious height, while the other is reduced virtually to ground level. The bailey appears to have occupied roughly the area of the present playing field. The remains of a stone tower were discovered in a service trench at its western corner.</p>
<p>The keep was uninhabitable by the mid 16th century.</p>
