<h1>Blaen Llynfi Castle</h1>
<div class="article-video right clearfix"/>
<p/><p>
<span style="font-size:18px;">This 13th century castle once had buttresses, towers and a moat.</span></p>
<p>Blaen Llynfi was built in the early 13th century and rebuilt in the middle of the 14th century. All that remains of the castle are scant late Norman ruins of a large bailey and ditch and a rubble curtain wall.</p>
<h2>Visiting Blaen Llynfi Castle</h2>
<p>Blaen Llynfi is open to the public. Access is free.</p>
<h3>How to get there</h3>
<p>The castle is one mile north of Bwlch, on the A4560.</p>
