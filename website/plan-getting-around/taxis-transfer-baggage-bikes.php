<p/><h2>National Park Knowledge</h2>
<p>The Brecon Beacons National Park Authority runs National Park Knowledge, a training scheme for local taxi drivers. Drivers who have attended the course have a good grounding in local history, activities and attractions, festivals and events. They display a sticker in their cabs, so you can identify them.</p>
<p>So far, courses have taken place in Brecon, Crickhowell, Abergavenny and Llandovery. They will be held in other towns in the National Park in due course.</p>
